/*
 * Exponentially-scaled modified Bessel functions of the first kind,
 * orders 0 and 1: i0e(x) = exp(-|x|) * I_0(x), i1e(x) = exp(-|x|) * I_1(x).
 *
 * Uses Cephes Chebyshev polynomial approximation (public domain, S. Moshier).
 * Same algorithm used by SciPy, JAX/XLA, PyTorch, and TensorFlow.
 *
 * Accuracy: float32 relative error < 2e-7 across full range.
 */

