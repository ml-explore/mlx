float i0e_impl(float x) {
  float ax = metal::abs(x);
  if (ax <= 8.0f) {
    float y = ax * 0.5f - 2.0f;
    return chbevl(y, A_i0);
  }
  return chbevl(32.0f / ax - 2.0f, B_i0) / metal::sqrt(ax);
}

float i1e_impl(float x) {
  float ax = metal::abs(x);
  float result;
  if (ax <= 8.0f) {
    float y = ax * 0.5f - 2.0f;
    result = chbevl(y, A_i1) * ax;
  } else {
    result = chbevl(32.0f / ax - 2.0f, B_i1) / metal::sqrt(ax);
  }
  return x < 0.0f ? -result : result;
}
